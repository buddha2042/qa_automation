SELECT
 CLAIM.CLAIM_NUMBER,
 REGION.LAST_NAME,
 CSH_STATUS_CODE.SHORT_CODE,
 CSH_STATUS_DESC.CODE_DESC,
 Round(sum(FINANCIAL_HIST.BALANCE),2),
 Round(sum(FINANCIAL_HIST.PAID),2),
 Round(sum(FINANCIAL_HIST.INCURRED),2)
FROM
 CODES_TEXT  EVENT_CATEGORY_DESC INNER JOIN CODES  EVENT_INDICATOR_CODE ON (EVENT_CATEGORY_DESC.CODE_ID=EVENT_INDICATOR_CODE.RELATED_CODE_ID)
  RIGHT OUTER JOIN EVENT ON (EVENT_INDICATOR_CODE.CODE_ID=EVENT.EVENT_IND_CODE)
  LEFT OUTER JOIN CLAIM ON (CLAIM.EVENT_ID=EVENT.EVENT_ID)
  LEFT OUTER JOIN CODES_TEXT  CLAIM_TYPE_DESC ON (CLAIM.CLAIM_TYPE_CODE=CLAIM_TYPE_DESC.CODE_ID)
  LEFT OUTER JOIN FINANCIAL_HIST ON (FINANCIAL_HIST.CLAIM_ID=CLAIM.CLAIM_ID)
  LEFT OUTER JOIN CLAIM_STATUS_HIST ON (CLAIM_STATUS_HIST.CLAIM_ID=CLAIM.CLAIM_ID)
  LEFT OUTER JOIN CODES_TEXT  CSH_STATUS_DESC ON (CLAIM_STATUS_HIST.STATUS_CODE=CSH_STATUS_DESC.CODE_ID)
  LEFT OUTER JOIN CODES  CSH_STATUS_CODE ON (CLAIM_STATUS_HIST.STATUS_CODE=CSH_STATUS_CODE.CODE_ID)
  LEFT OUTER JOIN CODES  CSH_OC_STATUS_CODE ON (CSH_OC_STATUS_CODE.CODE_ID=CSH_STATUS_CODE.RELATED_CODE_ID)
  LEFT OUTER JOIN CODES_TEXT  EVENT_TYPE_DESC ON (EVENT_TYPE_DESC.CODE_ID=EVENT.EVENT_TYPE_CODE)
  RIGHT OUTER JOIN (
 SELECT * FROM ENTITY WHERE ENTITY_TABLE_ID IN (SELECT TABLE_ID FROM GLOSSARY WHERE SYSTEM_TABLE_NAME = 'DEPARTMENT')
 )  DEPARTMENT ON (EVENT.DEPT_EID=DEPARTMENT.ENTITY_ID)
  RIGHT OUTER JOIN (
 SELECT * FROM ENTITY WHERE ENTITY_TABLE_ID IN (SELECT TABLE_ID FROM GLOSSARY WHERE SYSTEM_TABLE_NAME = 'FACILITY')
 )  FACILITY ON (DEPARTMENT.PARENT_EID=FACILITY.ENTITY_ID)
  RIGHT OUTER JOIN (
 SELECT * FROM ENTITY WHERE ENTITY_TABLE_ID IN (SELECT TABLE_ID FROM GLOSSARY WHERE SYSTEM_TABLE_NAME = 'LOCATION')
 
 )  LOCATION_ENTITY ON (FACILITY.PARENT_EID=LOCATION_ENTITY.ENTITY_ID)
  RIGHT OUTER JOIN (
 SELECT * FROM ENTITY WHERE ENTITY_TABLE_ID IN (SELECT TABLE_ID FROM GLOSSARY WHERE SYSTEM_TABLE_NAME = 'DIVISION')
 )  DIVISION ON (LOCATION_ENTITY.PARENT_EID=DIVISION.ENTITY_ID)
  RIGHT OUTER JOIN (
 SELECT * FROM ENTITY WHERE ENTITY_TABLE_ID IN (SELECT TABLE_ID FROM GLOSSARY WHERE SYSTEM_TABLE_NAME = 'REGION')
 
 )  REGION ON (DIVISION.PARENT_EID=REGION.ENTITY_ID)
 
WHERE
 (
  ( CLAIM_STATUS_HIST.CL_STATUS_ROW_ID IN
            (SELECT MAX(CL_STATUS_ROW_ID)
            FROM CLAIM_STATUS_HIST CSH2
            WHERE CLAIM_ID=CLAIM_STATUS_HIST.CLAIM_ID AND DATE_STATUS_CHGD IN
            (SELECT MAX(DATE_STATUS_CHGD) FROM CLAIM_STATUS_HIST CSH3
            WHERE CLAIM_ID=CSH2.CLAIM_ID AND convert (SMALLDATETIME, DATE_STATUS_CHGD)  <=  '02/29/2024 00:0:0' ))
 )
  AND
  REGION.LAST_NAME  IN  ('Florida Power & Light')
  AND
  convert (SMALLDATETIME, FINANCIAL_HIST.EVAL_DATE)  <=  '02/29/2024 00:0:0'
  AND
  EVENT_TYPE_DESC.SHORT_CODE  =  'VE'
  AND
  CLAIM_TYPE_DESC.SHORT_CODE   =  'BODI'
  AND
  CSH_OC_STATUS_CODE.SHORT_CODE  =  'O'
  AND
  convert(datetime,{fn left(CLAIM.DTTM_RCD_ADDED,8)})
 <=  cast(convert(char(10),getdate(),23) as datetime)
  AND
  EVENT_CATEGORY_DESC.CODE_DESC  IN  ( 'Fleet Accident','Non-Fleet Accident'  )
 )
GROUP BY
 CLAIM.CLAIM_NUMBER,
 REGION.LAST_NAME,
 CSH_STATUS_CODE.SHORT_CODE,
 CSH_STATUS_DESC.CODE_DESC
HAVING
 Round(sum(FINANCIAL_HIST.BALANCE),2)  =  0
UNION  
SELECT
 CLAIM.CLAIM_NUMBER,
 REGION.LAST_NAME,
 CSH_STATUS_CODE.SHORT_CODE,
 CSH_STATUS_DESC.CODE_DESC,
 Round(sum(FINANCIAL_HIST.BALANCE),2),
 Round(sum(FINANCIAL_HIST.PAID),2),
 Round(sum(FINANCIAL_HIST.INCURRED),2)
FROM
 CODES_TEXT  EVENT_CATEGORY_DESC INNER JOIN CODES  EVENT_INDICATOR_CODE ON (EVENT_CATEGORY_DESC.CODE_ID=EVENT_INDICATOR_CODE.RELATED_CODE_ID)
  RIGHT OUTER JOIN EVENT ON (EVENT_INDICATOR_CODE.CODE_ID=EVENT.EVENT_IND_CODE)
  LEFT OUTER JOIN CLAIM ON (CLAIM.EVENT_ID=EVENT.EVENT_ID)
  LEFT OUTER JOIN CODES_TEXT  CLAIM_TYPE_DESC ON (CLAIM.CLAIM_TYPE_CODE=CLAIM_TYPE_DESC.CODE_ID)
  LEFT OUTER JOIN FINANCIAL_HIST ON (FINANCIAL_HIST.CLAIM_ID=CLAIM.CLAIM_ID)
  INNER JOIN RESERVE_CURRENT  RESERVE_CURRENT_FH ON (RESERVE_CURRENT_FH.RC_ROW_ID=FINANCIAL_HIST.RC_ROW_ID and RESERVE_CURRENT_FH.CLAIM_ID=FINANCIAL_HIST.CLAIM_ID)
  LEFT OUTER JOIN CODES  FH_RESERVE_TYPE_CODE ON (FH_RESERVE_TYPE_CODE.CODE_ID=RESERVE_CURRENT_FH.RESERVE_TYPE_CODE)
  LEFT OUTER JOIN CLAIM_STATUS_HIST ON (CLAIM_STATUS_HIST.CLAIM_ID=CLAIM.CLAIM_ID)
  LEFT OUTER JOIN CODES_TEXT  CSH_STATUS_DESC ON (CLAIM_STATUS_HIST.STATUS_CODE=CSH_STATUS_DESC.CODE_ID)
  LEFT OUTER JOIN CODES  CSH_STATUS_CODE ON (CLAIM_STATUS_HIST.STATUS_CODE=CSH_STATUS_CODE.CODE_ID)
  LEFT OUTER JOIN CODES  CSH_OC_STATUS_CODE ON (CSH_OC_STATUS_CODE.CODE_ID=CSH_STATUS_CODE.RELATED_CODE_ID)
  LEFT OUTER JOIN CODES_TEXT  EVENT_TYPE_DESC ON (EVENT_TYPE_DESC.CODE_ID=EVENT.EVENT_TYPE_CODE)
  RIGHT OUTER JOIN (
 SELECT * FROM ENTITY WHERE ENTITY_TABLE_ID IN (SELECT TABLE_ID FROM GLOSSARY WHERE SYSTEM_TABLE_NAME = 'DEPARTMENT')
 )  DEPARTMENT ON (EVENT.DEPT_EID=DEPARTMENT.ENTITY_ID)
  RIGHT OUTER JOIN (
 SELECT * FROM ENTITY WHERE ENTITY_TABLE_ID IN (SELECT TABLE_ID FROM GLOSSARY WHERE SYSTEM_TABLE_NAME = 'FACILITY')
 )  FACILITY ON (DEPARTMENT.PARENT_EID=FACILITY.ENTITY_ID)
  RIGHT OUTER JOIN (
 SELECT * FROM ENTITY WHERE ENTITY_TABLE_ID IN (SELECT TABLE_ID FROM GLOSSARY WHERE SYSTEM_TABLE_NAME = 'LOCATION')
 
 )  LOCATION_ENTITY ON (FACILITY.PARENT_EID=LOCATION_ENTITY.ENTITY_ID)
  RIGHT OUTER JOIN (
 SELECT * FROM ENTITY WHERE ENTITY_TABLE_ID IN (SELECT TABLE_ID FROM GLOSSARY WHERE SYSTEM_TABLE_NAME = 'DIVISION')
 )  DIVISION ON (LOCATION_ENTITY.PARENT_EID=DIVISION.ENTITY_ID)
  RIGHT OUTER JOIN (
 SELECT * FROM ENTITY WHERE ENTITY_TABLE_ID IN (SELECT TABLE_ID FROM GLOSSARY WHERE SYSTEM_TABLE_NAME = 'REGION')
 
 )  REGION ON (DIVISION.PARENT_EID=REGION.ENTITY_ID)
 
WHERE
 (
  ( CLAIM_STATUS_HIST.CL_STATUS_ROW_ID IN
            (SELECT MAX(CL_STATUS_ROW_ID)
            FROM CLAIM_STATUS_HIST CSH2
            WHERE CLAIM_ID=CLAIM_STATUS_HIST.CLAIM_ID AND DATE_STATUS_CHGD IN
            (SELECT MAX(DATE_STATUS_CHGD) FROM CLAIM_STATUS_HIST CSH3
            WHERE CLAIM_ID=CSH2.CLAIM_ID AND convert (SMALLDATETIME, DATE_STATUS_CHGD)  <=  '02/29/2024 00:0:0' ))
 )
  AND
  REGION.LAST_NAME  IN  ('Florida Power & Light')
  AND
  convert (SMALLDATETIME, FINANCIAL_HIST.EVAL_DATE)  <=  '02/29/2024 00:0:0'
  AND
  EVENT_TYPE_DESC.SHORT_CODE  =  'VE'
  AND
  CLAIM_TYPE_DESC.SHORT_CODE   =  'BODI'
  AND
  CSH_OC_STATUS_CODE.SHORT_CODE  =  'O'
  AND
  FH_RESERVE_TYPE_CODE.SHORT_CODE  =  'BI'
  AND
  convert(datetime,{fn left(CLAIM.DTTM_RCD_ADDED,8)})
 <=  cast(convert(char(10),getdate(),23) as datetime)
  AND
  EVENT_CATEGORY_DESC.CODE_DESC  IN  ( 'Fleet Accident','Non-Fleet Accident'  )
 )
GROUP BY
 CLAIM.CLAIM_NUMBER,
 REGION.LAST_NAME,
 CSH_STATUS_CODE.SHORT_CODE,
 CSH_STATUS_DESC.CODE_DESC
HAVING
 Round(sum(FINANCIAL_HIST.BALANCE),2)  <  25000
